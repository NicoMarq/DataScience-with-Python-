#Ejercicio del gordo que camina 20 vueltas a la plaza por dia

#DEFINO VARIABLE INMEDIATA Y VARIABLE OBJETIVO PARA CORTAR EL LOOP

vueltas_ahora=0
vueltas_objetivo=20


    
while vueltas_ahora<21:
    print("YA CAMINÉ",vueltas_ahora, "VUELTAS")
    
    print("ME FALTAN:", vueltas_objetivo-vueltas_ahora,"VUELTAS")
       #DEFINO EL CONTADOR
    vueltas_ahora=vueltas_ahora+1
        
print("TERMINÉ , ME VOY A CASA")
      
   
    
 
